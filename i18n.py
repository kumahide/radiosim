def set_lang(lang: str) -> None:
    global _lang
    if lang in _STRINGS:
        _lang = lang


def t(key: str) -> str:
    """現在の言語でキーに対応する文字列を返す。未定義の場合は英語フォールバック。"""
    return _STRINGS.get(_lang, _STRINGS["en"]).get(key, _STRINGS["en"].get(key, key))
